// Comparison Operators

// Equal to

"3" == 3;
console.log("3" == 3);

// Strict Equal to
// Compares the datatype and value of the two values being checked

console.log("3" === 3);

// Not equal to
console.log("3" != 3); // false

// Strict no equal
console.log("3" !== 3); // true

// Greater than
console.log(3 > 2);

// Less than
console.log(2 < 3);

// Greater than or equal to
console.log(3 >= 4);

// Less than or equal to
console.log(2 <= 3);

// 'And' operator
2 && 3;

// 'Or' operator

2 || 3;
